void Matrix (char *Buffer, const char *message)
{
	unsigned char i, temp;
	for (char r=0; r<16;r++)
	*Buffer++=0;
	//*Buffer=1;
	while (*message!=0 && *message!=0x0d)
	{
		if (*message>=48 && *message<=57)
		{
			temp=*message-48;
			for (i=0;i<=5;i++)
			{
				*Buffer++=numero[temp][i];
			}
		}
		else if (*message>=65 && *message<=90)
		{
			temp=*message-65;
			for (i=0;i<=5;i++)
			{
				*Buffer++= mayuscula[temp][i];
			}
		}
		else if (*message>=97 && *message<=122)
		{
			temp=*message-97;
			for (i=0;i<=5;i++)
			{
				*Buffer++= minuscula[temp][i];
			}
		}
		else if (*message==32)
		{
			temp=*message-32;
			for (i=0;i<=5;i++)
			{
				*Buffer++= espacio[temp][i];
			}
		}
		else if (*message==32) //space

		{

			for(i=0;i<=4;i++)

			{

				*Buffer++=0;

			}

		}
		else if (*message==46)    //point

		{                                    //get the base number in the array numero (ASCII_CHAR) - (ASCII code for zero )

			*Buffer++=0x80;

			*Buffer++=0;

		}

		else if (*message==61)    //=

		{

			*Buffer++=0x28;

			*Buffer++=0x28;

			*Buffer++=0x28;

			*Buffer++=0x28;

			*Buffer++=0;

		}
		message+=1;
	}
	for (char r=0; r<16;r++)
	*Buffer++=0;
	*Buffer=0x0D;
	
}


